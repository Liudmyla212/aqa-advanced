// String
let variableString = 'Hello';
if (typeof variableString === 'string') {
    console.log("Variable is a string:", variableString);
} else {
    console.log("Variable is not a string");
}

// Number
let variableNumber = 212;
if (typeof variableNumber === 'number') {
    console.log("Variable is a number:", variableNumber);
} else {
    console.log("Variable is not a number");
}

// Boolean
let variableBoolean = false;
if (typeof variableBoolean === 'boolean') {
    console.log("Variable is a boolean:", variableBoolean);
} else {
    console.log("Variable is not a boolean");
}


// Null
let variableNull = null;
if (variableNull === null) {
    console.log("Variable is a null:", variableNull);
} else {
    console.log("Variable is not a null");
}

// Undefined
let variableUndefined; 
if (typeof variableUndefined === 'undefined') {
    console.log("Variable is undefined:", variableUndefined);
} else {
    console.log("Variable is not undefined");
}